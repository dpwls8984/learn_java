public class average {

    public static void main(String[] args) {
        int a = 2;
        int b = 3;
        int c = 6;
        int d = 8;

        int sum = a + b + c + d;
        int count = 4;

        double average = (double)sum / (double)count;

        System.out.println(average);
    }
}
